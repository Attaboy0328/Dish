function tagFor(category, protein) {
  const tags = []
  if (category === '荤菜') tags.push('下饭')
  if (category === '素菜') tags.push('清爽')
  if (category === '汤羹') tags.push('暖胃')
  if (category === '主食') tags.push('顶饱')
  if (category === '凉菜') tags.push('开胃')
  if (['猪肉', '牛肉', '羊肉', '禽类', '海鲜'].includes(protein)) tags.push('有肉')
  if (protein === '素' || protein === '蛋豆') tags.push('素口')
  tags.push('家常')
  if (Math.random() > 0.5) tags.push('快手')
  if (Math.random() > 0.7) tags.push('一人食')
  return [...new Set(tags)].slice(0, 4)
}

