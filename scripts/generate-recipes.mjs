function tagFor(category, protein) {
  const tags = []
  if (category === '荤菜') tags.push('下饭')
  if (category === '素菜') tags.push('清爽')
  if (category === '汤羹') tags.push('暖胃')
  if (category === '主食') tags.push('顶饱')
  if (category === '凉菜') tags.push('开胃')
  if (['猪肉', '牛肉', '羊肉', '禽类', '海鲜'].includes(protein)) tags.push('有肉')
  if (protein === '素' || protein === '蛋豆') tags.push('素口')
  tags.push('家常')
  if (Math.random() > 0.5) tags.push('快手')
  if (Math.random() > 0.7) tags.push('一人食')
  return [...new Set(tags)].slice(0, 4)
}

function ingredientsFor(name, protein, category) {
  const base = {
    猪肉: ['猪肉', '葱', '姜', '蒜', '生抽', '料酒'],
    牛肉: ['牛肉', '葱', '姜', '蒜', '生抽', '料酒'],
    羊肉: ['羊肉', '葱', '姜', '料酒', '八角'],
    禽类: ['鸡肉', '葱', '姜', '蒜', '生抽', '料酒'],
    海鲜: ['海鲜主料', '葱', '姜', '料酒', '盐'],
    蛋豆: ['鸡蛋或豆腐', '葱', '盐', '食用油'],
    素: ['时蔬', '蒜', '盐', '食用油'],
    其他: ['主料', '葱', '盐', '生抽'],
  }
  const ing = [...(base[protein] || base['其他'])]
  if (name.includes('番茄') || name.includes('西红柿')) ing.push('番茄')
  if (name.includes('土豆')) ing.push('土豆')
  if (name.includes('豆腐')) ing.push('豆腐')
  if (name.includes('排骨')) ing[0] = '排骨'
  if (name.includes('虾')) ing[0] = '虾'
  if (name.includes('鱼') && category !== '主食') ing[0] = '鱼'
  if (category === '汤羹') ing.push('清水')
  if (category === '主食' && name.includes('面')) ing.push('面条')
  if (category === '主食' && name.includes('饭')) ing.push('米饭')
  return [...new Set(ing)].slice(0, 7)
}

function stepsFor(name, category) {
  if (category === '汤羹') {
    return [
      `准备好食材，洗干净备用。`,
      `锅中加水，放入需要久炖的食材，大火烧开转小火。`,
      `撇去浮沫，继续炖 20–40 分钟至软烂。`,
      `加入配菜和调味，出锅前撒葱花即可。`,
    ]
  }
  if (category === '凉菜') {
    return [
      `主料洗净，按需要焯水或拍切。`,
      `调汁：蒜末、香醋、生抽、香油、少许糖与辣椒油。`,
      `浇汁拌匀，静置几分钟入味即可上桌。`,
    ]
  }
  if (category === '主食') {
    return [
      `准备主食与配料，米饭/面条提前备好。`,
      `热锅少油，先炒配料至断生。`,
      `下入主食翻炒均匀，调味（盐、生抽）。`,
      `大火收汁，起锅装盘。`,
    ]
  }
  return [
    `备齐食材：${name}所需主料切好，葱姜蒜切末。`,
    `热锅下油，爆香葱姜蒜，下主料翻炒至变色。`,
    `加入配菜继续炒，倒入生抽等调味，适量清水或淀粉勾芡。`,
    `大火收汁，装盘即可开吃。`,
  ]
}

function categoryLabel(listName) {
  return { hun: '荤菜', su: '素菜', tang: '汤羹', zhushi: '主食', liang: '凉菜' }[listName]
}

function uniqueFrom(items, listName) {
  const seen = new Set()
  const out = []
  for (const [name, protein, emoji] of items) {
    if (seen.has(name)) continue
    seen.add(name)
    out.push({
      sourceName: name,
      name,
      protein,
      emoji: emoji.length <= 2 ? emoji : '🍽️',
      category: categoryLabel(listName),
    })
  }
  return out
}

const all = [
  ...uniqueFrom(hun, 'hun'),
  ...uniqueFrom(su, 'su'),
  ...uniqueFrom(tang, 'tang'),
  ...uniqueFrom(zhushi, 'zhushi'),
  ...uniqueFrom(liang, 'liang'),
]

// Cross-category name dedupe: keep first occurrence
const usedNames = new Set()
const deduped = all.filter((item) => {
  if (usedNames.has(item.name)) return false
  usedNames.add(item.name)
  return true
})

/** Balanced pick across categories until TARGET recipes */
const TARGET = 500
const byCat = {
  荤菜: deduped.filter((d) => d.category === '荤菜'),
  素菜: deduped.filter((d) => d.category === '素菜'),
  汤羹: deduped.filter((d) => d.category === '汤羹'),
  主食: deduped.filter((d) => d.category === '主食'),
  凉菜: deduped.filter((d) => d.category === '凉菜'),
}
const ratios = { 荤菜: 0.34, 素菜: 0.24, 汤羹: 0.14, 主食: 0.18, 凉菜: 0.1 }
const quotas = Object.fromEntries(
  Object.entries(ratios).map(([cat, ratio]) => [cat, Math.round(TARGET * ratio)]),
)
// Fix rounding to exact TARGET
let quotaSum = Object.values(quotas).reduce((a, b) => a + b, 0)
while (quotaSum > TARGET) {
  quotas['荤菜'] -= 1
  quotaSum -= 1
}
while (quotaSum < TARGET) {
  quotas['荤菜'] += 1
  quotaSum += 1
}

const balanced = []
for (const cat of Object.keys(byCat)) {
  const want = Math.min(quotas[cat], byCat[cat].length)
  balanced.push(...byCat[cat].slice(0, want))
}
// Top up from leftovers if any category ran short
if (balanced.length < TARGET) {
  const taken = new Set(balanced.map((b) => b.name))
  for (const item of deduped) {
    if (balanced.length >= TARGET) break
    if (taken.has(item.name)) continue
    balanced.push(item)
    taken.add(item.name)
  }
}

const recipes = balanced.slice(0, TARGET).map((item, index) => {
  const timeMinutes =
    item.category === '汤羹' ? 35 + (index % 20) :
    item.category === '凉菜' ? 10 + (index % 10) :
    item.category === '主食' ? 20 + (index % 15) :
    15 + (index % 25)

  return {
    id: `r${String(index + 1).padStart(3, '0')}`,
    name: item.name,
    category: item.category,
    protein: item.protein,
    timeMinutes,
    difficulty: (1 + (index % 3)),
    tags: tagFor(item.category, item.protein),
    ingredients: ingredientsFor(item.sourceName, item.protein, item.category),
    steps: stepsFor(item.sourceName, item.category),
    emoji: item.emoji,
  }
})

const outDir = join(__dirname, '..', 'src', 'data')
mkdirSync(outDir, { recursive: true })
const outPath = join(outDir, 'recipes.json')
writeFileSync(outPath, JSON.stringify(recipes, null, 2), 'utf8')

const counts = recipes.reduce((acc, r) => {
  acc.cat[r.category] = (acc.cat[r.category] || 0) + 1
  return acc
}, { cat: {} })

